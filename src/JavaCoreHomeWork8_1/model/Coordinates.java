package JavaCoreHomeWork8_1.model;

public class Coordinates {           // координаты места
    Double lat;
    Double lon;

    public Coordinates() {
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLon() {
        return lon;
    }

    public void setLon(Double lon) {
        this.lon = lon;
    }
}